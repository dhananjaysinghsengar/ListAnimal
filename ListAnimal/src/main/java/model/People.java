package model;

public class People {

	public String name;
	public String gender;
	public float age;
	public Pet pets[];

	public String getName() {
		return name;
	}

	public String getGender() {
		return gender;
	}

	public float getAge() {
		return age;
	}

	public Pet[] getPets() {
		return pets;
	}

}
